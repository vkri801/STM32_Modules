/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	uint8_t u8_slaveAddr = 0x80;
	uint8_t Data = 0x30;
	uint8_t u8_recData[5] = {0};
	uint8_t u8_rxIndex = 0;
	int32_t s;

  /* USER CODE END 1 */
  

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_I2C1_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

	//Initialisation of I2C transfer - write
	LL_I2C_HandleTransfer(I2C1, u8_slaveAddr, LL_I2C_ADDRSLAVE_7BIT, 1, LL_I2C_MODE_AUTOEND, LL_I2C_GENERATE_START_WRITE);
	/*I2C bus type
	 * slave address
	 * type of address
	 * no. of bytes
	 * type of end
	 * generate write
	 */

	while(!LL_I2C_IsActiveFlag_STOP(I2C1)) //Wait for stop flag
	{
		if(LL_I2C_IsActiveFlag_TXE(I2C1)) //wait for empty TX
		{
			LL_I2C_TransmitData8(I2C1, Data); //transmit 'Data' as desired
		}
	}
	LL_I2C_ClearFlag_STOP(I2C1); // clear flag stop


	//Initialisation of I2C transfer - read
	while(!LL_I2C_IsActiveFlag_STOP(I2C1)) //Wait for stop flag
	{
		if(LL_I2C_IsActiveFlag_RXNE(I2C1)) //wait for RX flag
		{
			u8_recData[u8_rxIndex++]= LL_I2C_ReceiveData8(I2C1); //increment Rx index, write to variable
		}
	}
	u8_rxIndex = 0; //clear rxindex
	LL_I2C_ClearFlag_STOP(I2C1); //clear stop flag


	//Include code to do carry out task
	s = ((u8_recData[0]<<8) | u8_recData[1]); //sum LSB and MSB
  }
  /* USER CODE END 3 */
}

